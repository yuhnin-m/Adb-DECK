package com.adbdeck.core.adb.api

/**
 * Категория ошибки при проверке доступности внешнего инструмента.
 *
 * Значение используется UI-слоем для локализованного и более точного сообщения.
 */
enum class ToolCheckFailureKind {
    /** Запустился не тот бинарник (например, указан путь на adb вместо bundletool). */
    WRONG_EXECUTABLE,

    /** Процесс не удалось запустить (ошибка ProcessBuilder / права / файл не существует). */
    START_FAILED,

    /** Команда запустилась, но завершилась неуспешно. */
    COMMAND_FAILED,

    /** Причина не определена явно. */
    UNKNOWN,
}

